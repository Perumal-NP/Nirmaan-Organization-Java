package day7;

public class NestedForLoop {
public static void main(String [] args) {
	for(int i=0;i<=10;i++) {

          System.out.println(" ");
          for(int k=10;i<=k;k--) {
        	  System.out.print(" ");
        	  
          }
		for(int j=0;j<=i;j++) {
		   
			System.out.print("$");
			System.out.print(" ");
		}
	
	}
}
}
 